    }

    public string Fach
    {
        get => fach;
        set => fach = value;
    }

    public string Datum
    {
        get => datum;
        set => datum = value;
    }

    public string Teilleistung
    {
        get => teilleistung;
        set => teilleistung = value;
    }

    public string Note
    {
        get => note;
        set => note = value;
    }

    public string Bemerkung
